class NestedIterator:
    def __init__(self, nestedList: [NestedInteger]):
        self.list = self.flatten(nestedList)
    
    def flatten(self, currList) -> list:
        res = []
        for obj in currList:
            if obj.isInteger():
                res += [obj.getInteger()]
            else:
                res += self.flatten(obj.getList())
        return res
    
    def next(self) -> int:
        return self.list.pop(0)
    
    def hasNext(self) -> bool:
        if self.list:
            return True
        return False

